// Helper function to translate dropdown value
export const translateDropdownValue = (value, type, language) => {
  if (!value) return '';
  
  let mappings;
  switch (type) {
    case 'crop':
      mappings = cropTranslations;
      break;
    case 'season':
      mappings = seasonTranslations;
      break;
    case 'state':
      mappings = stateTranslations;
      break;
    default:
      return value;
  }
  
  // Return translated value if available, otherwise return original
  return mappings[language]?.[value] || value;
};
